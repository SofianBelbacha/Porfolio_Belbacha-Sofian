            <h2 class="text-center title-section mb-5">Réalisation d'un Site Web E-commerce Dynamique pour la Vente de Produits Hardware</h2>
            <p class="mt-4">Dans le cadre de mon BTS Services Informatiques aux Organisations (SIO), option Solutions Logicielles et Applications Métiers (SLAM), j'ai entrepris la conception et le développement d'un site web e-commerce dynamique destiné à la vente de produits hardware. Ce projet, réalisé en suivant le modèle MVC (Modèle-Vue-Contrôleur), a été construit en utilisant diverses technologies web, notamment HTML, CSS, JavaScript, Bootstrap, jQuery et PHP pour le backend.</p>

            <h3 class="mt-4">Fonctionnalités Principales</h3>
            <p>Le site web développé comporte plusieurs fonctionnalités clés permettant une expérience utilisateur riche et interactive, tout en offrant une gestion aisée des produits et des commandes pour les administrateurs.</p>

            <h4 class="mt-3">1. Parcourir les Produits par Catégories</h4>
            <p>Les utilisateurs peuvent naviguer à travers différents types de produits hardware, classés par catégories. Chaque catégorie affiche une liste de produits avec des détails tels que le nom, le prix, et une courte description. Cette fonctionnalité permet aux utilisateurs de trouver facilement les produits qu'ils recherchent.</p>
            <div class="d-flex justify-content-evenly flex-row col my-3">
                <img src="public/image/ap_web_categorie-produit" alt="alt" style="width: 30%;" class="col-3">
                <img src="public/image/ap_web_categorie-produit_2" alt="alt" style="width: 50%;">                
            </div>            
            <h4 class="mt-3">2. Ajouter des Produits au Panier</h4>
            <p>Chaque produit possède un bouton "Ajouter au panier", permettant aux utilisateurs de sélectionner les articles qu'ils souhaitent acheter. Cette action met à jour dynamiquement le contenu du panier sans recharger la page, offrant ainsi une expérience utilisateur fluide.</p>
            
            <img class="my-3" src="public/image/ap_web_panier.png" alt="alt" style="width: 50%;" class="col-3">

            <h4 class="mt-3">3. Gestion du Panier</h4>
            <p>Le panier d'achat est doté de plusieurs fonctionnalités pour gérer les produits ajoutés. Les utilisateurs peuvent modifier la quantité des articles, supprimer des produits du panier ou ajouter de nouveaux articles. Chaque modification est immédiatement reflétée, offrant une gestion en temps réel.</p>

            <h4 class="mt-3">4. Passer des Commandes</h4>
            <p>Une fois les produits sélectionnés, les utilisateurs peuvent passer commande. Ce processus inclut la récapitulation des produits, la saisie des informations de livraison et de paiement, et la confirmation de la commande. Un email de confirmation est envoyé à l'utilisateur après la validation de la commande.</p>

            <h4 class="mt-3">5. Authentification des Utilisateurs</h4>
            <p>Le site propose des fonctionnalités d'inscription et de connexion pour les utilisateurs. Les nouveaux utilisateurs peuvent créer un compte en fournissant des informations de base, tandis que les utilisateurs existants peuvent se connecter pour accéder à leur compte, consulter l'historique des commandes et gérer leurs informations personnelles.</p>

