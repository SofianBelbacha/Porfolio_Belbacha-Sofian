            <h4 class="mt-3">6. Interface d'Administration</h4>
            <p>Pour les administrateurs, une interface dédiée permet de gérer les produits et les commandes. Les administrateurs peuvent ajouter, modifier ou supprimer des produits, consulter les commandes passées, et mettre à jour le statut des commandes. Cette interface est sécurisée et accessible uniquement aux utilisateurs ayant les droits d'administration.</p>

            <img class="my-3" src="public/image/partie_admin_web.png" alt="alt" style="width: 50%;" class="col-3">

            <h4 class="mt-4">Technologies Utilisées</h4>
            <ul>
                <li><strong>HTML</strong> : Structure de base des pages web.</li>
                <li><strong>CSS</strong> : Stylisation des éléments pour une présentation visuelle attrayante.</li>
                <li><strong>JavaScript & jQuery</strong> : Interactions dynamiques et manipulation du DOM.</li>
                <li><strong>Bootstrap</strong> : Mise en page réactive et composants prêts à l'emploi pour un design moderne.</li>
                <li><strong>PHP</strong> : Traitement côté serveur, logique applicative et interactions avec la base de données.</li>
                <li><strong>MVC (Modèle-Vue-Contrôleur)</strong> : Organisation du code pour une séparation claire des préoccupations, facilitant la maintenance et l'évolutivité du site.</li>
            </ul>
